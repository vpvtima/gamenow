<h2></h2>

<p>В игре присутствуют 7 основных мировых религий? <a href="https://ru.wikipedia.org/wiki/%D0%91%D1%83%D0%B4%D0%B4%D0%B8%D0%B7%D0%BC" title="Буддизм">буддизм</a>, <a href="https://ru.wikipedia.org/wiki/%D0%98%D0%BD%D0%B4%D1%83%D0%B8%D0%B7%D0%BC" title="Индуизм">индуизм</a>, <a href="https://ru.wikipedia.org/wiki/%D0%98%D1%81%D0%BB%D0%B0%D0%BC" title="Ислам">ислам</a>, <a href="https://ru.wikipedia.org/wiki/%D0%A5%D1%80%D0%B8%D1%81%D1%82%D0%B8%D0%B0%D0%BD%D1%81%D1%82%D0%B2%D0%BE" title="Христианство">христианство</a>, <a href="https://ru.wikipedia.org/wiki/%D0%98%D1%83%D0%B4%D0%B0%D0%B8%D0%B7%D0%BC" title="Иудаизм">иудаизм</a>, <a href="https://ru.wikipedia.org/wiki/%D0%9A%D0%BE%D0%BD%D1%84%D1%83%D1%86%D0%B8%D0%B0%D0%BD%D1%81%D1%82%D0%B2%D0%BE" title="Конфуцианство">конфуцианство</a>, <a href="https://ru.wikipedia.org/wiki/%D0%94%D0%B0%D0%BE%D1%81%D0%B8%D0%B7%D0%BC" title="Даосизм">даосизм</a>.</p>

<p>Все религии дают одинаковый эффект на развитие цивилизации.</p>

<p>Религия основывается в одном из городов державы, первой изучившей определённую технологию. При распространении религии в одном из городов цивилизации её можно объявить государственной. Города, где распространена государственная религия, получают дополнительные очки счастья и производства. Кроме того, эффект от религии можно увеличить за счёт принятия определённой парадигмы и возведения в городах религиозных зданий.</p>

<p>Для распространения религии могут быть созданы специальные юниты &mdash; <a href="https://ru.wikipedia.org/wiki/%D0%9C%D0%B8%D1%81%D1%81%D0%B8%D0%BE%D0%BD%D0%B5%D1%80" title="Миссионер">миссионеры</a>.</p>

<p>Великий пророк может создать в городе, в котором основана религия, священное для этой религии здание.</p>

