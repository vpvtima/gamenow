<p><strong>Sid Meier&#39;s Civilization IV</strong> &mdash; <a href="https://ru.wikipedia.org/wiki/%D0%9F%D0%BE%D1%88%D0%B0%D0%B3%D0%BE%D0%B2%D1%8B%D0%B5_%D1%81%D1%82%D1%80%D0%B0%D1%82%D0%B5%D0%B3%D0%B8%D0%B8" title="Пошаговые стратегии">пошаговая стратегия</a> <a href="https://ru.wikipedia.org/wiki/Firaxis_Games" title="Firaxis Games">Firaxis Games</a>, продолжение популярной серии <a href="https://ru.wikipedia.org/wiki/Civilization" title="Civilization">Civilization</a>(предыдущей была <a href="https://ru.wikipedia.org/wiki/Civilization_III" title="Civilization III">Civilization III</a>). Релиз игры состоялся <a href="https://ru.wikipedia.org/wiki/25_%D0%BE%D0%BA%D1%82%D1%8F%D0%B1%D1%80%D1%8F" title="25 октября">25 октября</a> <a href="https://ru.wikipedia.org/wiki/2005_%D0%B3%D0%BE%D0%B4" title="2005 год">2005 года</a> в США и Канаде и <a href="https://ru.wikipedia.org/wiki/4_%D0%BD%D0%BE%D1%8F%D0%B1%D1%80%D1%8F" title="4 ноября">4 ноября</a> в Европе. Интерфейс игры претерпел радикальные изменения, карта стала анимированной. <a href="https://ru.wikipedia.org/wiki/24_%D0%B8%D1%8E%D0%BD%D1%8F" title="24 июня">24 июня</a> <a href="https://ru.wikipedia.org/wiki/2006_%D0%B3%D0%BE%D0%B4" title="2006 год">2006 года</a>вышел аддон <a href="https://ru.wikipedia.org/wiki/Civilization_IV:_Warlords" title="Civilization IV: Warlords">Warlords</a>. Второе дополнение &mdash; <a href="https://ru.wikipedia.org/wiki/Civilization_IV:_Beyond_the_Sword" title="Civilization IV: Beyond the Sword">Beyond the Sword</a> вышло 27 июня 2007 года. В Beyond the Sword 10 новых цивилизаций, 12 новых сценариев, 16 новых правителей и 5 новых чудес света.</p>

<h2>Описание игры</h2>

<p>Sid Meier&#39;s Civilization 4 &mdash; продолжение одной из самых популярных и успешных серий глобальных пошаговых стратегий. Суммарный тираж серии в данный момент насчитывает более 6 млн копий. В <a href="https://ru.wikipedia.org/wiki/2009_%D0%B3%D0%BE%D0%B4" title="2009 год">2009 году</a> сайт <a href="https://ru.wikipedia.org/wiki/IGN" title="IGN">IGN</a>поставил Civilization 4 на третье место в ряду 25 лучших PC-игр всех времён<sup><a href="https://ru.wikipedia.org/wiki/Sid_Meier%27s_Civilization_IV#cite_note-1">1</a></sup>.</p>

<h2>Суть игры</h2>

<p>В игре вам даётся возможность управлять одной из империй. Вы начинаете игру в <a href="https://ru.wikipedia.org/wiki/4004_%D0%B3%D0%BE%D0%B4_%D0%B4%D0%BE_%D0%BD._%D1%8D." title="4004 год до н. э.">4000 году до н. э.</a> с поселенцем и одним воином (или разведчиком, если начальная технология &mdash; Охота), с вами будут также находиться ещё несколько цивилизаций, туземные деревни и племена варваров. Перед началом игры предоставляется возможность выбрать тип карты (<a href="https://ru.wikipedia.org/wiki/%D0%9F%D0%B0%D0%BD%D0%B3%D0%B5%D1%8F" title="Пангея">пангея</a>, несколько континентов, островная карта и т. п.), а также её размер, от которого зависит количество соперничающих с вами цивилизаций. Также перед началом игры доступен выбор климата и уровня моря, что влияет на ландшафт сгенерированной карты и находящиеся на ней ресурсы. Каждый ход в игре равен определённому количеству лет, что зависит от выбранной игроком скорости игры. Кроме того, имеется определённый масштаб, по которому чем ближе игрок к современности, тем меньше лет в одном ходе и наоборот. При низких скоростях игры, в недалёком прошлом, современности и будущем, один год может проходить за несколько ходов. В игре вы имеете несколько путей к победе:</p>

<ul>
	<li><strong>Победа по очкам</strong></li>
</ul>

<p>Игра заканчивается в 2050 году и побеждает цивилизация, набравшая большее количество очков.</p>

<ul>
	<li><strong>Победа завоеванием</strong></li>
</ul>

<p>Игра заканчивается, когда какая-либо цивилизация уничтожает всех соперников. Государство уничтожается тогда, когда у него не остаётся больше городов, независимо от находящихся под его контролем юнитов. Также можно ввести настройку &laquo;Биться до последнего&raquo;, то есть цивилизация уничтожается только при уничтожении всех её городов и юнитов.</p>

<ul>
	<li><strong>Победа доминированием</strong></li>
</ul>

<p>Игра заканчивается, когда цивилизация имеет под контролем 25 % всего населения и 56 % всей территории суши.</p>

<ul>
	<li><strong>Победа культурой</strong></li>
</ul>

<p>Игра заканчивается, когда нация имеет 3 города с Легендарной культурой (в русской версии не слишком удачный перевод &laquo;легендарный&raquo; как &laquo;максимальный&raquo; уровень культуры: 50 000 очков культуры на город при &laquo;нормальной&raquo; скорости игры).</p>

<ul>
	<li><strong>Победа в космической гонке</strong></li>
</ul>

<p>Игра заканчивается, когда одна из цивилизаций посылает корабль на <a href="https://ru.wikipedia.org/wiki/%D0%90%D0%BB%D1%8C%D1%84%D0%B0_%D0%A6%D0%B5%D0%BD%D1%82%D0%B0%D0%B2%D1%80%D0%B0" title="Альфа Центавра">Альфу Центавра</a> и он успешно достигает цели. В отличие от предыдущей игры серии, нужно, чтобы космический корабль именно достиг Альфы Центавра, а не просто был запущен: шанс долететь тем выше, чем больше компонентов корабля построено; корабль может быть запущен раньше и с неполным возможным комплектом всех модулей, но шанс достижения цели полёта ниже.</p>

<ul>
	<li><strong>Победа дипломатией</strong></li>
</ul>

<p>Игра завершается, когда при выборах в <a href="https://ru.wikipedia.org/wiki/%D0%9E%D0%9E%D0%9D" title="ООН">ООН</a> государство выигрывает выборы мирового лидера и добивается принятия всеми странами дипломатических соглашений.</p>

<p>После победы, если ваши достижения были достаточно эффективны, ваш счёт заносят в Зал славы. Также, после победы, есть возможность продолжить игру. В таком случае последующий счёт не будет заноситься в Зал славы и не будет возможности добиться победы иного типа.</p>

<h2></h2>

<p>По карте игрового мира рассредоточены различные ресурсы. Некоторые из них (например, лошади, золото, пшеница и др.) являются открытыми с самого старта игры; другие же появляются на карте после изучения определённой технологии (например, при старте игры невидимы залежи железа, однако после изучения игроком обработки железа оно появляется на карте).</p>

<p>В отличие от предыдущей части игры ресурсы, расположенные за пределами культурного влияния государства, не могут быть включены в торговую сеть.</p>

<h2></h2>

<p>По сравнению с предыдущими частями, бои претерпели значительные изменения.</p>

<p>Теперь у каждой боевой единицы есть параметр <a href="https://ru.wikipedia.org/w/index.php?title=%D0%9E%D0%BF%D1%8B%D1%82_(%D0%B8%D0%B3%D1%80%D1%8B)&amp;action=edit&amp;redlink=1" title="Опыт (игры) (страница отсутствует)">опыт</a>. Участвуя в битвах, юнит набирает очки опыта и с повышением уровня может потратить его на изучение одного апгрейда. Также опыт дают некоторые строения (такие как <a href="https://ru.wikipedia.org/wiki/%D0%9A%D0%B0%D0%B7%D0%B0%D1%80%D0%BC%D1%8B" title="Казармы">казармы</a>, <a href="https://ru.wikipedia.org/wiki/%D0%A1%D1%83%D1%85%D0%BE%D0%B9_%D0%B4%D0%BE%D0%BA" title="Сухой док">сухой док</a> и т. п.) и парадигмы. В более поздних аддонах к игре, очки опыта при производстве может давать также выдающаяся личность &mdash; генерал, поселённый в городе. Не имеет значения, защищался отряд или нападал, в любом случае при победе ему присуждаются очки опыта. Но стоит помнить, что воздушные отряды опыт не накапливают (это было реализовано только в аддоне Beyond the Sword), также он не даётся за бомбардировки или в случае, когда вражеский юнит отступил. Не каждый отряд имеет все продвижения по службе, некоторые можно получить только при выполнения определённого условия, другие же могут использовать только определённые боевые единицы.</p>

<p>Всего в игре 41 различное продвижение по службе.</p>

